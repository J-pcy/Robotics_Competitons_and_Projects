/*********************************************************** 
函数名称：LCD_init
函数功能：5110初始化
入口参数：无
出口参数：无 
备 注： 
***********************************************************/
void LCD_init(void)
{
    //引脚初始化
    gpio_init(LCD_RST, GPO, 0);
    gpio_init(LCD_CE, GPO, 0);
    gpio_init(LCD_DC, GPO, 0);
    gpio_init(LCD_DIN, GPO, 0);
    gpio_init(LCD_SCLK, GPO, 0);
    gpio_init(LCD_BL, GPO, 1);
    
    gpio_set(LCD_RST,0);           //产生一个让LCD复位的低电平脉冲
    systick_delay(200);            //延时1us
    gpio_set(LCD_RST,1); 
    
    gpio_set(LCD_CE,0);            //关闭LCD
    systick_delay(200);            //延时1us
    gpio_set(LCD_CE,1); 
    systick_delay(200);            //延时1us
    
    LCD_write_byte(0x21, 0);	// 使用扩展命令设置LCD模式
    LCD_write_byte(0xc8, 0);	// 设置液晶偏置电压
    LCD_write_byte(0x06, 0);	// 温度校正
    LCD_write_byte(0x13, 0);	// 1:48
    LCD_write_byte(0x20, 0);	// 使用基本命令，V=0，水平寻址
    
    LCD_clear();	        // 清屏
    LCD_write_byte(0x0c, 0);	// 设定显示模式，正常显示
    gpio_set(LCD_CE,0);         // 关闭LCD
    
}
/*********************************************************** 
函数名称：LCD_write_byte
函数功能：模拟SPI接口时序写数据/命令LCD
入口参数：data    ：写入的数据；
          command ：写数据/命令选择；
出口参数：无 
备 注： 
***********************************************************/
void LCD_write_byte(uint8 dat,uint8 command)
{
    uint8 i; 
    gpio_set(LCD_CE,0);				 //5110片选有效，允许输入数据
    if (command==0)				 //写命令
    {     gpio_set(LCD_DC,0);}		  
    else 
    {     gpio_set(LCD_DC,1);}			 //写数据
    for(i=0;i<8;i++)				 //传送8bit数据
    { 
      if(dat&0x80)
      {   gpio_set(LCD_DIN,1);}
      else
      {   gpio_set(LCD_DIN,0);}
      gpio_set(LCD_SCLK,0);
      dat = dat << 1;
      gpio_set(LCD_SCLK,1);
    }
     gpio_set(LCD_CE,1);			 //禁止5110
}
/*********************************************************** 
函数名称：LCD_clear
函数功能：清屏
入口参数：无
出口参数：无 
备 注： 
***********************************************************/
void LCD_clear(void)
{
  uint16 i;
  LCD_write_byte(0x0c,0);
  LCD_write_byte(0x80,0);
  for(i=0;i<504;i++)
  {
    LCD_write_byte(0,1);
  }
}
/*********************************************************** 
函数名称：LCD_set_XY
函数功能：设置LCD坐标函数
入口参数：X       ：0－83
          Y       ：0－5
出口参数：无 
备 注： 
***********************************************************/
void LCD_set_XY(uint8 X, uint8 Y)
{
    LCD_write_byte(0x40 | Y, 0);		  // column
    LCD_write_byte(0x80 | X, 0);        // row
}
/*********************************************************** 
函数名称：LCD_write_char
函数功能：显示英文字符
入口参数：c	:  显示的字符
出口参数：无 
备 注： 
***********************************************************/
void LCD_write_char(uint8 c)
{
    uint8 line;
    c -= 32;      //数组的行号
    for(line=0;line<6;line++)
      LCD_write_byte(ASCII_6_8[c][line], 1);
}





void LCD_write_english_string(unsigned char X,unsigned char Y,char *s) 
{ 
    LCD_set_XY(X,Y); 
    while (*s)  
    { 
     LCD_write_char(*s); 
     s++; 
    } 
} 









